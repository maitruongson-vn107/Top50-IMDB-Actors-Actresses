class Actor:

    def __init__(self, actorID, name, birth_name, nick_name, dob, origin, height, image, bio):
        self.actorID = actorID
        self.name = name
        self.birth_name = birth_name
        self.nick_name = nick_name
        self.dob = dob
        self.origin = origin
        self.height = height
        self.image = image
        self.bio = bio
